    private void update(int enem) {
        
        this.enem = enem;
        AbstractButton boton;
        String[] separator = null , separator2 = null;
        
        if(memento != null) {
        	separator = memento.split("\n");
        	if(separator.length > enem + 2) {
        		separator2 = separator[enem + 2].split("T");
            }
        }
        
        var p = buttonGroup3.getElements();
        
        if(enem == numjugador){
            for(int i = 0; i < 100 ; i++){
                boton = p.nextElement();
                boton.setText("");
                String name = boton.getName();
                if(separator2 != null) {
                	for(int j = 1 ; j < separator2[0].split(";").length ; j++){
                        for(int b = 1; b < Character.getNumericValue(separator2[0].split(";")[j].charAt(1)) + 1 ; b++){
                            if(separator2[0].split(";")[j].split("_")[b].split(",")[1].equalsIgnoreCase(name)){
                                boton.setText(separator2[0].split(";")[j].split("_")[b].split(",")[0]);
                            }
                        }
                    }
                    if(separator2.length == 2){
                        for(int h = 0 ; h < separator2[1].split("_").length ; h++){
                            if(separator2[1].split("_")[h].split(",")[1].equalsIgnoreCase(name)){
                                boton.setText(separator2[1].split("_")[h].split(",")[0]);
                            }
                        }
                    }
                }
            }
        }
        else{
           for(int i = 0; i < 100 ; i++){
                boton = p.nextElement();
                String name = boton.getName();
                boton.setText("");
                if(separator2 != null) {
                	if(separator2.length == 2){
                        for(int j = 0 ; j < separator2[1].split("_").length ; j++){
                            if(separator2[1].split("_")[j].split(",")[1].equalsIgnoreCase(name)){
                                boton.setText(separator2[1].split("_")[j].split(",")[0]);
                            }
                        }
                    }
                }
            }
        }
        
       
    }

